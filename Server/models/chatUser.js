import pkg from "mongoose";
const { Schema, model, models } = pkg;
import bcrypt from "bcrypt";

const ChatUserSchema = new Schema({
  username: {
    type: String,
    required: true,
    min: 3,
    max: 20,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    max: 50,
  },
  password: {
    type: String,
    required: true,
    min: 8,
  },
  isAvatarImageSet: {
    type: Boolean,
    default: false,
  },
  avatarImage: {
    type: String,
    default: "",
  },
});

const ChatUser = models.ChatUser || model("ChatUser", ChatUserSchema);

export default ChatUser;
